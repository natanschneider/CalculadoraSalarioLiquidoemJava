    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

       SalarioBruto = Double.parseDouble(txtSalarioBruto.getText());
       
       SBProveitos.setText(String.valueOf(String.format("%.2f", SalarioBruto)));
       
       // Calculo INSS
       
    if(SalarioBruto <= 1212){
        INSS = 0.075 * SalarioBruto;
        INSSDesconto.setText("R$ " + String.format("%.2f", INSS));
        INSSAlicota.setText("7,5%");
    }else if(SalarioBruto >= 1212.01 && SalarioBruto <= 2427.35){
        INSS = 0.09 * SalarioBruto;
        INSSDesconto.setText("R$ " + String.format("%.2f", INSS));
        INSSAlicota.setText("9%");
    }else if(SalarioBruto >= 2427.36 && SalarioBruto <= 3641.03){
        INSS = 0.12 * SalarioBruto;
        INSSDesconto.setText("R$ " + String.format("%.2f", INSS));
        INSSAlicota.setText("12%");
    }else if(SalarioBruto >= 3641.04 && SalarioBruto <= 7087.22){
        INSS = 0.14 * SalarioBruto;
        INSSDesconto.setText("R$ " + String.format("%.2f", INSS));
        INSSAlicota.setText("14%");
    }else if(SalarioBruto >= 7087.23){
        INSS = 992.21;
        INSSDesconto.setText("R$ " + String.format("%.2f", INSS));
        INSSAlicota.setText("TETO");
    }
    
       // Calculo IRPF
    
    if(SalarioBruto >= 1903.99 && SalarioBruto <= 2826.65){
        IRPF = 0.075 * SalarioBruto;
        IRPFDesconto.setText("R$ " + String.format("%.2f", IRPF));
        IRPFAlicota.setText("7.50%");
    }else if(SalarioBruto >= 2826.66 && SalarioBruto <= 3751.05){
        IRPF = 0.15 * SalarioBruto;
        IRPFDesconto.setText("R$ " + String.format("%.2f", IRPF));
        IRPFAlicota.setText("15%");
    }else if(SalarioBruto >= 3751.06 && SalarioBruto <= 4664.68){
        IRPF = 0.255 * SalarioBruto;
        IRPFDesconto.setText("R$ " + String.format("%.2f", IRPF));
        IRPFAlicota.setText("22.50%");
    }else if(SalarioBruto >= 4664.69){
        IRPF = 0.275 * SalarioBruto;
        IRPFDesconto.setText("R$ " + String.format("%.2f", IRPF));
        IRPFAlicota.setText("27.50%");
    }else if(SalarioBruto <= 1903.98){
        IRPFDesconto.setText("Isento");
        IRPFAlicota.setText("0%");
    }
    // Calcular Salario Familia
    
    if(SalarioBruto <= 1655.98){
        Dependentes = Double.parseDouble(txtDependentes.getText());
        
        SalarioFamilia = Dependentes * 56.47;
    
        SFProveitos.setText("R$ " + String.valueOf(String.format("%.2f", SalarioFamilia)));
    }else{
        SFProveitos.setText("NÃ£o valido");
        
    }
    
    Desconto = IRPF + INSS;
    Proveito = SalarioBruto + SalarioFamilia;
    SalLiquido = Proveito - Desconto;
    
    // Totais
    txtSalarioLiquido.setText(String.valueOf(String.format("%.2f", SalLiquido)));
    TotalProveitos.setText("R$ " + String.valueOf(String.format("%.2f", Proveito)));
    TotalDesconto.setText("R$ " + String.valueOf(String.format("%.2f", Desconto)));

    }//GEN-LAST:event_btnCalcularActionPerformed

